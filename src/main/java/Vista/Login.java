    private void jButtonIngresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonIngresarMouseClicked
        // Obtener los valores de usuario y contraseña
        String usuario = jTextFieldUsuario.getText();
        String password = new String(jTextFieldPassword.getPassword());
        String tipo = DatabaseUtils.verifyLogin(usuario,password);
        if (tipo != null) {
            switch (tipo) {
                case "administrativo":{
                    MenuAdmin menuAdmin = new MenuAdmin(principal);
                    principal.mostrarPanel(menuAdmin);
                    break;
                }
                case "profesor":{
                    MenuProfesores menuProfesores = new MenuProfesores(principal);
                    principal.mostrarPanel(menuProfesores);
                    break;
                }
                case "alumno":{
                    MenuAlumnos menuAlumnos = new MenuAlumnos(principal);
                    principal.mostrarPanel(menuAlumnos);
                    break;
                }
            }

        } else {
            JOptionPane.showMessageDialog(Login.this, "Email o password equivocado.");
        }
        
        
    }//GEN-LAST:event_jButtonIngresarMouseClicked

